/**## **Ejercicio 9 — Ciclo while**
**Descripción:** Imprime los números del 10 al 1.
**Objetivo:** Usar while.
**Ejemplo/Guía:** */

let contador = 10;

while (contador>=1) {
    console.log(contador);
    contador--;
    
}

let booleano = true;

while (booleano) {
    for(let i = 0; i<10; i++){
        console.log(i);
        
    }
    booleano=false;
    
}