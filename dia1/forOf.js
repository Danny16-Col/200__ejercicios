/**## **Ejercicio 16 — for...of**
**Descripción:** Recorre un array con for...of.
**Objetivo:** Usar for...of.
**Ejemplo/Guía:** */

let animales = ["perro","gato","loro"];

for (let i of animales) {
    console.log(i);
    
}

let Personas = ["Carlos","Daniel","Cortes","Rios"]

for(let i of Personas){
    console.log(i);
    
}