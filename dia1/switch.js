/**## **Ejercicio 7 — Switch**
**Descripción:** Muestra el día de la semana según un número.
**Objetivo:** Practicar switch/case.
**Ejemplo/Guía:** */
let n = 2;

switch (n) {
    case 0: 
        console.log("domingo");
        break;
    case 1: 
        console.log("lunes");
        break;
    case 2: 
        console.log("martes");
        break;
    case 3: 
        console.log("miercoles");
        break;
    case 4: 
        console.log("jueves");
        break;
    case 5: 
        console.log("viernes");
        break;
    case 6: 
        console.log("sabado");
        break;
    default:
        break;
}