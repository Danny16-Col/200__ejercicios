/**## **Ejercicio 6 — Condicional múltiple**
**Descripción:** Verifica si un número es positivo, negativo o cero.
**Objetivo:** Practicar if / else if / else.
**Ejemplo/Guía:** */

const n = -1;

if (n===0) {
    console.log(`el ${n} es un numero neutro`);
    
}else if (n<0) {
    console.log(`el ${n} es negativo`);
    
}else{
    console.log(`el ${n} es positivo`);

}

