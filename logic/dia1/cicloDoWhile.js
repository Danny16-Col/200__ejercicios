/**## **Ejercicio 10 — Ciclo do...while**
**Descripción:** Pide un número hasta que sea mayor a 10.
**Objetivo:** Usar do...while.
**Ejemplo/Guía:** */
let n = 11;

do {
    console.log(`numero ${n}`);
    n++;
    
} while (n<10);


let booleano = true;

do {
    for(let i = 1; i<10; i++){
        console.log(`${i}`);
        
    }

    booleano=false;
} while (false);