/**## **Ejercicio 11 — Función simple**
**Descripción:** Crear una función que sume dos números.
**Objetivo:** Practicar function.
**Ejemplo/Guía:** */

function suma(a,b){
    return a+b;

}

console.log(`Suma: ${suma(2,5)}`);


function multiplicar (a,b){

    return a*b
}

console.log(`${multiplicar(3,5)}`);
